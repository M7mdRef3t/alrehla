/**
 * يرجع سؤال اليوم بناءً على رقم اليوم في السنة
 * يتغير كل 24 ساعة عند منتصف الليل
 */
export function getQuestionOfDay(): DailyQuestion {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  const index = dayOfYear % DAILY_QUESTIONS.length;
  return DAILY_QUESTIONS[index];
}

/**
 * مفتاح التخزين اليومي — يتغير كل يوم عشان نعرف إذا أجاب المستخدم النهارده ولا لأ
 */
export function getTodayStorageKey(): string {
  const now = new Date();
  const dateStr = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, "0")}-${String(now.getDate()).padStart(2, "0")}`;
  return `dawayir-daily-${dateStr}`;
}
