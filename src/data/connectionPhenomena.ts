/** البحث عن أقرب ظاهرة لوصف المستخدم */
export function classifyUserExperience(description: string): ConnectionPhenomenonType | null {
  const keywords: Record<ConnectionPhenomenonType, string[]> = {
    mirror_neurons: ["حسيت بألمه", "اتألمت معاه", "دمعت عيني", "تألم"],
    neural_sync: ["نفس الكلام", "نفس الجملة", "قال اللي في بالي", "تزامن"],
    somatic_intuition: ["حسيت إن حاجة هتحصل", "معدتي", "إحساس في جسمي", "حدس"],
    frequency_illusion: ["فكرت فيه واتصل", "تخاطر", "تكرار", "بشوفه في كل مكان"],
    cold_reading: ["عرفت من وشه", "حسيت إنه بيكذب", "فهمت من غير ما يقول"],
    remote_sensing: ["حسيت بيه وهو بعيد", "حلمت بيه", "قلقت عليه", "إحساس عن بعد"],
  };

  const lower = description.toLowerCase();
  let bestMatch: ConnectionPhenomenonType | null = null;
  let bestScore = 0;

  for (const [type, words] of Object.entries(keywords) as [ConnectionPhenomenonType, string[]][]) {
    const score = words.filter(w => lower.includes(w)).length;
    if (score > bestScore) {
      bestScore = score;
      bestMatch = type;
    }
  }

  return bestMatch;
}

