/** ترجمة مصطلح إنجليزي للبديل العربي البسيط */
export function translateScientificTerm(englishTerm: string): string {
  const found = SCIENTIFIC_TERMS_ARABIC.find(
    (t) => t.english.toLowerCase() === englishTerm.toLowerCase()
  );
  return found ? found.arabic : englishTerm;
}

/** البحث عن آية مناسبة لسياق معين */
export function findQuranicLinkForContext(useCase: string): QuranicScientificLink | undefined {
  return QURANIC_SCIENTIFIC_LINKS.find((link) =>
    link.useCases.some((uc) => useCase.includes(uc) || uc.includes(useCase))
  );
}

/** بناء بلوك القاموس للـ prompts */
export function buildTruthLexiconBlock(): string {
  const lines: string[] = [
    "**قاموس المصطلحات (إلزامي):**",
    "",
    "مصطلحات ممنوعة وبدائلها:",
    ...DAJJAL_ALTERNATIVES.alternatives.map(
      (a) => `- بدل "${DAJJAL_ALTERNATIVES.forbidden}" → "${a.term}" (${a.context})`
    ),
    ...QATL_ALTERNATIVES.alternatives.map(
      (a) => `- بدل "قتل" → "${a.term}" (${a.context})`
    ),
    "",
    "مصطلحات علمية بالعربي:",
    ...SCIENTIFIC_TERMS_ARABIC.map(
      (t) => `- بدل "${t.english}" → "${t.arabic}" (${t.simpleExplanation})`
    ),
    "",
    `**الموقف المعرفي:** ${EPISTEMOLOGICAL_STANCE.principle}`,
    ...EPISTEMOLOGICAL_STANCE.rules.map((r) => `- ${r}`),
  ];
  return lines.join("\n");
}
