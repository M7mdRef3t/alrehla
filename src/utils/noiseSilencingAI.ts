/**
 * بروتوكول إسكات الضجيج — تحليل واقعي للحوارات التخيلية (Mental Arguments).
 * الهدف: تصغير "البعبع" في الخيال وتحديد الحاجة غير المُلباة.
 */

import { geminiClient } from "@/services/geminiClient";

/**
 * إدخال: نص الاجترار أو ملخص ما يريد المستخدم قوله للشخص.
 * المخرجات: رد بارد ومنطقي — الحاجة غير المُلباة + تصغير الكارثة.
 */
export async function realityCheckImaginaryArgument(
  rantText: string,
  personLabel?: string
): Promise<string | null> {
  const who = personLabel ? `الشخص المتخيل في الحوار: "${personLabel}".` : "شخص في خيالك.";
  const prompt = `أنت مرساة واقع (Reality Anchor) في منصة "الرحلة" للتعافي العاطفي. المستخدم يعاني من "حوارات تخيلية" — يخناق في رأسه شخصًا لا يسمعه.

${who}

ما قاله المستخدم (اجترار / غضب / عتاب):
---
${rantText || "(قام بالتفريغ الصوتي فقط ولم يكتب نصًا)"}
---

◈ المحور الرأسي: الاجترار = البحث عن طاقة من مصدر بشري (مراية). المصدر الحقيقي هو ربنا/المصدر. لو الضجيج عالي، ممكن الجذر يكون انقطاع روحي.

مهامك بإيجاز شديد وبالعربية — استخدم "Validation Sandwich" (مهم جداً):
1. صدمة القبول (أول جملة): ابدأ بتعاطف — "حقك تحس بكده، ده شعور طبيعي لما تحس بـ [الحاجة غير المُلباة]." عشان تهدى دفاعاته قبل المنطق.
2. الواقع: إيه أسوأ حاجة تحصل فعلياً لو اتهمك أو قال لك أسوأ شيء؟ (مجرد كلمة؟ مش موت ولا سجن.)
3. التمكين: "إنت مش محتاج تستناه عشان تحس بـ [الحاجة]، إنت تقدر تديه لنفسك دلوقتي بـ [خطوة بسيطة]." لو مناسب، لمّح بلطف للاتصال بالمصدر كمرسى للسلام.

كن هادئًا، مختصرًا (3–5 جمل قصيرة)، وحنونًا. لا تدخل مباشرة للمنطق — التعاطف أولاً يفتح الباب.
استخدم: "ربنا"، "المصدر"، "السلام الداخلي". تجنب: "الدين"، "العبادة"، "الإسلام".`;
  return geminiClient.generate(prompt);
}
