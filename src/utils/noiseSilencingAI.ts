/**
 * بروتوكول إسكات الضجيج — تحليل واقعي للحوارات التخيلية (Mental Arguments).
 * الهدف: تصغير "البعبع" في الخيال وتحديد الحاجة غير المُلباة.
 */

import { geminiClient } from "../services/geminiClient";

/**
 * إدخال: نص الاجترار أو ملخص ما يريد المستخدم قوله للشخص.
 * المخرجات: رد بارد ومنطقي — الحاجة غير المُلباة + تصغير الكارثة.
 */
export async function realityCheckImaginaryArgument(
  rantText: string,
  personLabel?: string
): Promise<string | null> {
  const who = personLabel ? `الشخص المتخيل في الحوار: "${personLabel}".` : "شخص في خيالك.";
  const prompt = `أنت مرساة واقع (Reality Anchor) في تطبيق تعافي عاطفي. المستخدم يعاني من "حوارات تخيلية" — يخناق في رأسه شخصًا لا يسمعه.

${who}

ما قاله المستخدم (اجترار / غضب / عتاب):
---
${rantText || "(قام بالتفريغ الصوتي فقط ولم يكتب نصًا)"}
---

مهامك بإيجاز شديد وبالعربية:
1. حدد الحاجة غير المُلباة وراء الخناقة (اعتراف، احترام، حدود، أمان، إلخ).
2. قل له بوضوح: لو اتهمك أو قال لك أسوأ شيء — إيه أسوأ حاجة تحصل فعلياً؟ (هل ستموت؟ تدخل السجن؟ أم مجرد كلمة؟)
3. اختتم بجملة واحدة: أنه يمكنه منح نفسه هذه الحاجة الآن دون انتظار الطرف الآخر.

كن هادئًا، مختصرًا (3–5 جمل قصيرة)، ولا تلقّن. الهدف: تصغير البعبع وإعادة المرساة للواقع.`;
  return geminiClient.generate(prompt);
}
