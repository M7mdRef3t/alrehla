// ─── Public Getters ───────────────────────────────────────────────

export function getDomainDeepContent(domainId: LifeDomainId): DomainDeepContent {
  return DOMAIN_DEEP_CONTENT[domainId];
}

export function getAllDomainExercises(): DomainExercise[] {
  return Object.values(DOMAIN_DEEP_CONTENT).flatMap(d => d.exercises);
}

export function getRandomExerciseForDomain(domainId: LifeDomainId): DomainExercise {
  const exercises = DOMAIN_DEEP_CONTENT[domainId].exercises;
  return exercises[Math.floor(Math.random() * exercises.length)];
}
