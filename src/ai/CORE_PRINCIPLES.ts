/**
 * دالة مساعدة: تحقق إذا محتوى معين متوافق مع المبادئ
 */
export function isAlignedWithPrinciples(content: string): {
  aligned: boolean;
  violations: string[];
} {
  const violations: string[] = [];

  // Check 1: Toxic Positivity
  const toxicPhrases = [
    "كل شيء هيبقى تمام",
    "فكر إيجابي بس",
    "ابتسم والدنيا هتضحك ليك",
    "متزعلش",
    "الحمد لله على كل حال",
  ];
  if (toxicPhrases.some((p) => content.includes(p))) {
    violations.push("toxic_positivity");
  }

  // Check 2: Quick Fixes
  const quickFixPhrases = ["5 خطوات", "الحل السحري", "جرب كذا وكل شيء يتحل"];
  if (quickFixPhrases.some((p) => content.includes(p))) {
    violations.push("quick_fix");
  }

  // Check 3: Guru Speak
  const guruPhrases = ["أنا عندي الحل", "اسمع مني", "لازم تعمل كذا"];
  if (guruPhrases.some((p) => content.includes(p))) {
    violations.push("guru_speak");
  }

  // Check 4: Medical Diagnosis
  const diagnosisPhrases = [
    "عندك depression",
    "ده bipolar",
    "أنت مريض",
    "محتاج دواء",
  ];
  if (diagnosisPhrases.some((p) => content.includes(p))) {
    violations.push("medical_diagnosis");
  }

  return {
    aligned: violations.length === 0,
    violations,
  };
}

/**
 * دالة مساعدة: احسب مدى توافق المحتوى مع "صوت محمد"
 * (تُستخدم في AI evaluation)
 */
export function calculateVoiceAlignment(content: string): number {
  let score = 5; // البداية من النص

  // +1 لو فيه استعارة من المفردات المفضلة
  const metaphors = AI_GENERATION_RULES.useMetaphors;
  if (metaphors.some((m) => content.includes(m))) {
    score += 1;
  }

  // +1 لو السؤال reflective (فيه "ليه" أو "إزاي" أو "إيه")
  if (content.includes("ليه") || content.includes("إزاي") || content.includes("إيه")) {
    score += 1;
  }

  // +1 لو فيه validation language ("طبيعي"، "مفهوم"، "عادي")
  if (
    content.includes("طبيعي") ||
    content.includes("مفهوم") ||
    content.includes("عادي")
  ) {
    score += 1;
  }

  // +1 لو مفيش أوامر (لو فيه "لازم" أو "يجب" = -1)
  if (content.includes("لازم") || content.includes("يجب")) {
    score -= 1;
  } else {
    score += 1;
  }

  // +1 لو في نطاق الطول المناسب
  const words = content.split(" ").length;
  if (words >= 10 && words <= 30) {
    score += 1;
  }

  return Math.max(0, Math.min(score, 10));
}
