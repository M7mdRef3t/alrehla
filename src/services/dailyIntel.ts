export const getQuestionOfDay = (): string => {
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const diff = now.getTime() - start.getTime();
    const oneDay = 1000 * 60 * 60 * 24;
    const dayOfYear = Math.floor(diff / oneDay);

    const questionIndex = dayOfYear % tacticalQuestions.length;
    return tacticalQuestions[questionIndex];
};
